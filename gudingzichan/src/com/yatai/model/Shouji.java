package com.yatai.model;

public class Shouji {
	private int id;
	private String jishenma;
	private String chukudanhao;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getJishenma() {
		return jishenma;
	}
	public void setJishenma(String jishenma) {
		this.jishenma = jishenma;
	}
	public String getChukudanhao() {
		return chukudanhao;
	}
	public void setChukudanhao(String chukudanhao) {
		this.chukudanhao = chukudanhao;
	}

	
}
